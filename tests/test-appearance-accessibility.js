const fs=require('fs');
const vm=require('vm');
const assert=require('assert');
const app=fs.readFileSync('app.js','utf8');
const html=fs.readFileSync('index.html','utf8');
const css=fs.readFileSync('styles.css','utf8');
assert(app.includes("const APP_VERSION = window.WATCHVERSE_VERSION || '1.0.0'"));
assert(app.includes("name:'Watchverse Original'"));
assert(app.includes("id:'system'"));
assert(app.includes("id:'compact'"));
assert(app.includes('function renderAccessibility()'));
assert(app.includes('function renderAccessibilityReport()'));
assert(html.includes('id="footerVersion"'));
assert(html.includes('href="#/accessibility"'));
assert(html.includes('accessibility-report.js'));
assert(css.includes('html[data-density="compact"]'));
assert(css.includes('html[data-theme="editorial-light"]'));
const sandbox={window:{}}; vm.createContext(sandbox); vm.runInContext(fs.readFileSync('accessibility-report.js','utf8'),sandbox);
const report=sandbox.window.WATCHVERSE_WCAG_REPORT;
assert.equal(report.version,'1.0.0');
assert.equal(report.criteria.length,86);
assert.equal(new Set(report.criteria.map(x=>x.id)).size,86);
assert.deepEqual([...new Set(report.criteria.map(x=>x.status))].sort(),['failed','not-applicable','not-verified','passed']);
const target=report.criteria.filter(x=>x.level==='A'||x.level==='AA');
assert.equal(target.length,55);
assert.equal(target.filter(x=>x.status==='failed').length,3);
console.log('✓ Aspetto, footer e report WCAG verificati');

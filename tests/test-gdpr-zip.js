'use strict';
const assert=require('assert');
const fs=require('fs');
const vm=require('vm');
const path=require('path');
const base=path.resolve(__dirname,'..');
const app=fs.readFileSync(path.join(base,'app.js'),'utf8');
const start=app.indexOf('  async function inflateZipBytes');
const end=app.indexOf('\n\n  const crcTable',start);
assert(start>=0&&end>start,'Funzioni ZIP non trovate in app.js');
const snippet=app.slice(start,end);
const pako=require(path.join(base,'assets/vendor/pako_inflate.min.js'));
const sandbox={console,Uint8Array,DataView,TextDecoder,Blob,Response,DecompressionStream,globalThis:null,pako,setTimeout};
sandbox.globalThis=sandbox;
vm.createContext(sandbox);
vm.runInContext(`${snippet}\nthis.__zip={inflateZipBytes,readZipEntries};`,sandbox);
const buf=fs.readFileSync(path.join(__dirname,'fixture-tvtime-gdpr.zip'));
const file={name:'fixture-tvtime-gdpr.zip',size:buf.length,arrayBuffer:async()=>buf.buffer.slice(buf.byteOffset,buf.byteOffset+buf.byteLength)};
(async()=>{
  const progress=[];
  const entries=await sandbox.__zip.readZipEntries(file,(done,total,name)=>progress.push({done,total,name}));
  assert(entries.length>=5);
  assert(entries.some(e=>e.name.endsWith('tracking-prod-records-v2.csv')));
  assert(progress.length>0);
  const GDPR=require(path.join(base,'gdpr-import.js'));
  const plan=await GDPR.buildPlanAsync(entries);
  assert.strictEqual(plan.counts.series,1);
  assert.strictEqual(plan.counts.episodes,1);
  assert.strictEqual(plan.counts.movies,1);
  assert.strictEqual(plan.counts.watchlistMovies,0);
  assert(plan.ignoredSensitive.includes('access_token.csv'));
  console.log('✓ ZIP GDPR: decompressione Pako, progress e anteprima conteggi');
})().catch(e=>{console.error(e);process.exit(1)});
